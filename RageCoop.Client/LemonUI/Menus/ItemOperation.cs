namespace LemonUI.Menus
{
    /// <summary>
    /// The operation performed when the menu items are modified.
    /// </summary>
    public enum ItemOperation
    {
        /// <summary>
        /// The item has been removed.
        /// </summary>
        Removed = -1,
        /// <summary>
        /// The item has been added.
        /// </summary>
        Added = 0,
    }
}
